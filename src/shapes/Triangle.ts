/**
 *
 * @param base
 * @param height
 */
export const triangleArea = (base: number, height: number): number => 0.5 * base * height

/**
 *
 * @param sideX
 * @param sideY
 * @param base
 */
export const trianglePerimeter = (sideX: number, sideY: number, base: number): number => sideX + sideY + base




